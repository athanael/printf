#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int		i;

	i = 0;
	if (!f)
		return ;
	if (!s)
		return ;
	while (s[i])
	{
		f(&s[i]);
		i++;
	}
}
