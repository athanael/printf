#include "libft.h"

int		ft_strequ(const char *s1, const char *s2)
{
	int		i;

	if (!s1 || !s2)
		return (0);
	i = -1;
	while (s1[++i] || s2[i])
		if (s1[i] != s2[i] || !s1[i])
			return (0);
	return (1);
}
