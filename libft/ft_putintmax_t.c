#include "libft.h"

void	ft_putintmax_t(intmax_t nb)
{
	if (nb % 10 + '0' == '(')
	{
		ft_putstr("9223372036854775808");
		return ;
	}
	if (nb % 10 + '0' == '/')
	{
		ft_putchar('1');
		return ;
	}
	if (nb / 10 > 0)
		ft_putintmax_t(nb / 10);
	ft_putchar(nb % 10 + '0');
}
