#include "libft.h"

void	ft_putlong_long(long long nb)
{
	if (nb < 0)
	{
		nb = nb * (-1);
		ft_putchar('-');
	}
	if (nb % 10 + '0' == '(')
	{
		ft_putstr("9223372036854775808");
		return ;
	}
	if (nb / 10 > 0)
		ft_putlong_long(nb / 10);
	ft_putchar(nb % 10 + '0');
}
