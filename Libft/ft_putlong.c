#include "libft.h"

void	ft_putnbr(long nb)
{
	if (nb == -2147483648)
	{
		ft_putchar('-');
		ft_putnbr(2147483648);
		return ;
	}
	if (nb < 0)
	{
		nb = nb * (-1);
		ft_putchar('-');
	}
	if (nb / 10 > 0)
		ft_putnbr(nb / 10);
	ft_putchar(nb % 10 + '0');
}
